void LCD_DrawTriangle(void) {
	int x, y;
	LCD_DrawLine(180, 100, 80, Horizontal);
	//x坐标的变化范围[120,180]
	for (x = 120; x <= 180; x++) {
		y = 2 * x / 3 - 20;		//根据两点计算得直线方程式
		LCD_SetCursor(x, y);	//定位光标到(x,y)
		LCD_WriteRAM_Prepare();	//写RAM准备
		LCD_WriteRAM(White);	//显示前景色
	}

	for (x = 120; x <= 180; x++) {
		y = -2 * x / 3 + 140;
		LCD_SetCursor(x, y);
		LCD_WriteRAM_Prepare();
		LCD_WriteRAM(White);
	}
}

void PrintName(int x, int y, unsigned char data[]) {
	int i, j, k;
	unsigned char temp;
	for (i = 0; i < ROW; i++) {
		for (j = 0; j < COL; j++) {
			temp = data[COL * i + j];
			for (k = 0; k < 8; k++) {
				LCD_SetCursor(x + i, y + (8 * ROW + 8) - (8 * j + k));
				LCD_WriteRAM_Prepare();
				if ((temp & (1 << k)) != 0)
					LCD_WriteRAM(White);
				else
					LCD_WriteRAM(Blue);
			}
		}
	}
}

void Delay_Ms(uint32_t nTime)
{
	TimingDelay = nTime;
	while (TimingDelay != 0);
}
